int	ft_countparts(const char *s, char c)
{
	int	cnt;
	int	subs;

	subs = 0;
	cnt = 0;
	while (*s != '\0')
	{
		if (subs == 1 && *s == c)
			subs = 0;
		if (subs == 0 && *s != c)
		{
			subs = 1;
			cnt++;
		}
		s++;
	}
	return (cnt);
}
